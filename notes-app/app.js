const chalk = require('chalk')
const yargs = require('yargs')
const notes = require('./notes.js') // Importez votre propre module 'notes' qui contient des fonctions pour manipuler les notes.

// Customize yargs version
yargs.version('1.1.0')

// Create add command
yargs.command({
    command: 'add',
    describe: 'Add a new note',
    builder: {
        title: {
            describe: 'Note title',
            demandOption: true,
            type: 'string'
        },
        body: {
            describe: 'Note body',
            demandOption: true,
            type: 'string'
        }
    },
    handler: function (argv) {
        notes.addNote(argv.title, argv.body) // Appelle la fonction 'addNote' pour ajouter une nouvelle note, en utilisant les données fournies par l'utilisateur.
    }
}) // Créez une commande 'add' qui permet aux utilisateurs d'ajouter de nouvelles notes.


// Create remove command
yargs.command({
    command: 'remove',
    describe: 'Remove a note',
    handler: function () {
        console.log('Removing the note')
    }
})

// Create list command
yargs.command({
    command: 'list',
    describe: 'List your notes',
    handler: function () {
        console.log('Listing out all notes')
    }
})

// Create read command
yargs.command({
    command: 'read',
    describe: 'Read a note',
    handler: function () {
        console.log('Reading a note')
    }
})

yargs.parse()